package stack;

public class Stack {

  private int size;

  private int top;

  private int[] S;

  private char[] C;

  public int getSize() {
    return size;
  }

  public void setSize(int size) {
    this.size = size;
  }

  public int getTop() {
    return top;
  }

  public void setTop(int top) {
    this.top = top;
  }

  public int[] getS() {
    return S;
  }

  public void setS(int[] s) {
    S = s;
  }

  public char[] getC() {
    return C;
  }

  public void setC(char[] c) {
    C = c;
  }
}
